#include<bits/stdc++.h>
using namespace std;

int main() {
    for(int i = 1; i<=5; i++) {
        cout << i << "\n";
    }

    int j = 5;
    while (j>=1) {
        cout << j << "\n";
        j--;
    }

    int k = 10;
    do {
        cout << k << "\n";
        k--;
    }while(k>10);
    return 0;
}